@dataclass
class CodeGenContext:
    """Context for code generation."""
    indent_level: int = 1
    current_window: Optional[str] = None
    in_event_loop: bool = False
    opened_windows: List[str] = field(default_factory=list)
    idcmp_flags: Set[str] = field(default_factory=set)
    # Stack of scopes - each scope is a set of variable names declared in that scope
    scope_stack: List[Set[str]] = field(default_factory=lambda: [set()])

    @property
    def indent(self) -> str:
        return "    " * self.indent_level

    def enter_scope(self):
        """Enter a new scope."""
        self.scope_stack.append(set())

    def exit_scope(self):
        """Exit current scope."""
        if len(self.scope_stack) > 1:
            self.scope_stack.pop()

    def declare_var(self, name: str):
        """Declare a variable in the current scope."""
        self.scope_stack[-1].add(name)

    def is_declared(self, name: str) -> bool:
        """Check if variable is declared in any scope."""
        for scope in self.scope_stack:
            if name in scope:
                return True
        return False

    def is_declared_in_current_scope(self, name: str) -> bool:
        """Check if variable is declared in current scope only."""
        return name in self.scope_stack[-1]


class CodeGenerator(ASTVisitor):
    """
    Generates C code from the Dash AST.

    Uses templates for clean, maintainable code generation.
    """

    def __init__(self, analysis: AnalysisResult):
        self.analysis = analysis
        self.context = CodeGenContext()
        self.globals: List[str] = []
        self.prototypes: List[str] = []
        self.functions: List[str] = []
        self.struct_typedefs: List[str] = []  # Track struct type definitions
        self.enum_defines: List[str] = []  # Track enum definitions
        self.const_defines: List[str] = []  # Track constant definitions
        self.extern_protos: List[str] = []  # Track extern function prototypes
        self.native_funcs: List[str] = []  # Track native function definitions
        self.lambda_funcs: List[str] = []  # Track generated lambda functions
        self.lambda_counter: int = 0  # Counter for unique lambda names
        self.main_code: List[str] = []
        self.current_function: Optional[FunctionDef] = None
        self.uses_arrays: bool = False  # Track if arrays are used
        self.uses_strings: bool = False  # Track if string operations are used
        self.uses_windows: bool = False  # Track if windows are used
        self.uses_refcount: bool = False  # Track if refcount runtime is needed
        self.uses_arenas: bool = False  # Track if arena runtime is needed
        self.var_types: Dict[str, str] = {}  # Track variable types for string/struct detection
        # Track Amiga library requirements
        self.needs_gfx: bool = False
        self.needs_audio: bool = False
        self.needs_asl: bool = False
        self.needs_fileio: bool = False
        self.needs_gadtools: bool = False
        self.needs_reaction: bool = False  # Track if ReAction is used
        self.needs_area: bool = False  # Track if Area fill functions are used
        self.needs_rtg: bool = False  # Track if RTG (CyberGraphX/P96) is used
        # Track libraries needed for extern functions
        self.extern_libraries: Set[str] = set()
        # Track struct variables in current scope for automatic release
        self.scope_struct_vars: List[List[str]] = [[]]  # Stack of scopes with struct var names
        # Custom headers from @header directives
        self.custom_headers: List[str] = []

    def generate(self) -> str:
        """Generate the complete C program."""
        # Pre-scan to detect Area fill usage before code generation
        self._prescan_for_area(self.analysis.program)
        self.visit(self.analysis.program)
        return self._assemble_program()

    def _prescan_for_area(self, node: ASTNode):
        """Pre-scan AST to detect if Area fill functions are used."""
        area_funcs = {"gfx_ellipse_fill", "gfx_circle_fill",
                      "gfx_poly_begin", "gfx_poly_add", "gfx_poly_end"}

        def scan(n):
            if isinstance(n, FunctionCall) and n.name in area_funcs:
                self.needs_area = True
                return  # Found, no need to continue
            # Recursively scan children
            for attr in dir(n):
                if attr.startswith('_'):
                    continue
                value = getattr(n, attr, None)
                if isinstance(value, ASTNode):
                    scan(value)
                elif isinstance(value, list):
                    for item in value:
                        if isinstance(item, ASTNode):
                            scan(item)

        scan(node)

    def _assemble_program(self) -> str:
        """Assemble all parts into the final program."""
        parts = [CTemplates.PROGRAM_HEADER]

        # Add graphics includes if needed
        if self.needs_gfx:
            parts.append("#include <proto/graphics.h>\n")
            parts.append("#include <graphics/gfxbase.h>\n")
            parts.append("#include <graphics/displayinfo.h>\n")
            parts.append("#include <string.h>\n")

        # Add audio includes if needed
        if self.needs_audio:
            parts.append("#include <devices/audio.h>\n")
            parts.append("#include <hardware/custom.h>\n")

        # Add ASL includes if needed
        if self.needs_asl:
            parts.append("#include <proto/asl.h>\n")

        # Add GadTools includes if needed
        if self.needs_gadtools:
            parts.append("#include <proto/gadtools.h>\n")

        # Add custom headers from @header directives
        if self.custom_headers:
            parts.append("\n/* Custom Headers */\n")
            for header in self.custom_headers:
                parts.append(header + "\n")

        # Add array runtime if needed
        if self.uses_arrays:
            parts.append(CTemplates.ARRAY_RUNTIME)

        # Add string runtime if needed
        if self.uses_strings:
            parts.append(CTemplates.STRING_RUNTIME)

        # Add reference counting runtime if needed (before structs)
        if self.uses_refcount:
            parts.append(CTemplates.REFCOUNT_RUNTIME)

        # Add arena allocator runtime if needed
        if self.uses_arenas:
            parts.append(CTemplates.ARENA_RUNTIME)

        # Add constant definitions
        if self.const_defines:
            parts.append("\n/* Constants */\n")
            parts.append("\n".join(self.const_defines))
            parts.append("\n")

        # Add enum definitions
        if self.enum_defines:
            parts.append("\n/* Enum Definitions */\n")
            parts.append("\n".join(self.enum_defines))
            parts.append("\n")

        # Add struct type definitions
        if self.struct_typedefs:
            parts.append("\n/* Struct Definitions */\n")
            parts.append("\n".join(self.struct_typedefs))
            parts.append("\n")

        # Add window runtime if needed (for _dash_win, _dash_rp, _dash_screen)
        # Also needed when using ReAction (for screen support)
        if self.uses_windows or self.needs_reaction:
            parts.append(CTemplates.WINDOW_RUNTIME)

        # Add graphics runtime if needed
        if self.needs_gfx:
            parts.append(CTemplates.GRAPHICS_RUNTIME)

        # Add input runtime if needed (for key/mouse/joystick)
        if self.needs_gfx:
            parts.append(CTemplates.INPUT_RUNTIME)

        # Add GadTools runtime if needed
        if self.needs_gadtools:
            parts.append(CTemplates.GADTOOLS_RUNTIME)
            parts.append(CTemplates.MENU_RUNTIME)

        # Add ASL runtime if needed
        if self.needs_asl:
            parts.append(CTemplates.ASL_RUNTIME)

        # Add File I/O runtime if needed
        if self.needs_fileio:
            parts.append(CTemplates.FILEIO_RUNTIME)

        # Add ReAction runtime if needed
        if self.needs_reaction:
            parts.append(CTemplates.REACTION_RUNTIME)

        # Add RTG runtime if needed
        if self.needs_rtg:
            parts.append(CTemplates.RTG_RUNTIME)

        # Add global declarations
        if self.globals:
            parts.append("\n".join(self.globals))

        # Add extern function prototypes
        if self.extern_protos:
            parts.append("\n// Extern Function Prototypes\n")
            parts.append("\n".join(self.extern_protos))

        # Add function prototypes
        if self.prototypes:
            parts.append("\n// Function Prototypes")
            parts.append("".join(self.prototypes))

        # Add native function definitions
        if self.native_funcs:
            parts.append("\n// Native Function Definitions")
            parts.append("\n".join(self.native_funcs))

        # Add lambda function definitions
        if self.lambda_funcs:
            parts.append("\n// Lambda Function Definitions")
            parts.append("".join(self.lambda_funcs))

        # Add function definitions
        if self.functions:
            parts.append("\n// Function Definitions")
            parts.append("\n".join(self.functions))

        # Generate main with appropriate library initialization
        main_template = self._get_main_template()
        user_code = "".join(self.main_code)
        parts.append(main_template.substitute(user_code=user_code))

        return "\n".join(parts)

    def _get_main_template(self) -> Template:
        """Get the appropriate main template based on features used."""
        if self.needs_gadtools:
            return CTemplates.PROGRAM_MAIN_GADTOOLS
        if self.needs_gfx:
            return CTemplates.PROGRAM_MAIN_GFX
        return CTemplates.PROGRAM_MAIN

    def emit(self, code: str):
        """Emit code to the current output."""
        self.main_code.append(code)

    def emit_global(self, code: str):
        """Emit code to the globals section."""
        self.globals.append(code)

    # === Visitor Methods ===

    def visit_Program(self, node: Program):
        """Generate code for the program."""
        # Store custom headers from @header directives
        self.custom_headers = node.headers if node.headers else []

        # Generate constant definitions
        for const in node.constants:
            self._generate_const_def(const)

        # Generate global variable declarations
        for gvar in node.global_vars:
            self._generate_global_var(gvar)

        # Generate enum definitions
        for enum in node.enums:
            self._generate_enum_def(enum)

        # Generate struct type definitions
        for struct in node.structs:
            self._generate_struct_typedef(struct)

        # Generate extern function prototypes
        for extern in node.extern_funcs:
            self._generate_extern_proto(extern)

        # Generate native function definitions
        for native in node.native_funcs:
            self._generate_native_func(native)

        # Generate window globals
        for window in node.windows:
            self.visit_WindowDef_global(window)

        # Generate function definitions
        for func in node.functions:
            self.visit(func)

        # Generate main block code
        if node.main:
            self.visit(node.main)

    def _generate_const_def(self, const: ConstDecl):
        """Generate C #define for a constant."""
        value = const.value
        if isinstance(value, NumberLiteral):
            self.const_defines.append(f"#define {const.name} {value.value}")
        elif isinstance(value, StringLiteral):
            self.const_defines.append(f"#define {const.name} {value.value}")
        elif isinstance(value, BoolLiteral):
            c_val = "TRUE" if value.value else "FALSE"
            self.const_defines.append(f"#define {const.name} {c_val}")

    def _generate_global_var(self, gvar: GlobalVarDecl):
        """Generate C global variable declaration."""
        # Map Dash types to C types
        type_map = {
            "Int": "LONG",
            "String": "STRPTR",
            "Bool": "BOOL",
            "Pointer": "APTR",
        }
        c_type = type_map.get(gvar.var_type, "LONG")

        # Generate initial value if present
        if gvar.initial_value:
            init_val = self._generate_expression(gvar.initial_value)
            self.emit_global(f"{c_type} {gvar.name} = {init_val};\n")
        else:
            # Default initialization
            if c_type == "STRPTR":
                self.emit_global(f"{c_type} {gvar.name} = NULL;\n")
            elif c_type == "APTR":
                self.emit_global(f"{c_type} {gvar.name} = NULL;\n")
            elif c_type == "BOOL":
                self.emit_global(f"{c_type} {gvar.name} = FALSE;\n")
            else:
                self.emit_global(f"{c_type} {gvar.name} = 0;\n")

        # Track variable type for later use
        if c_type == "STRPTR":
            self.var_types[gvar.name] = "string"
        else:
            self.var_types[gvar.name] = "number"

        # Mark as declared in global scope
        self.context.declare_var(gvar.name)

    def _generate_enum_def(self, enum: EnumDef):
        """Generate C enum definition."""
        lines = [f"typedef enum {{"]
        current_value = 0
        for i, val in enumerate(enum.values):
            if val.value is not None:
                current_value = val.value
            comma = "," if i < len(enum.values) - 1 else ""
            lines.append(f"    {enum.name}_{val.name} = {current_value}{comma}")
            current_value += 1
        lines.append(f"}} {enum.name};")
        self.enum_defines.append("\n".join(lines))

    def _generate_extern_proto(self, extern: ExternFunc):
        """Generate extern function prototype."""
        # Track library needed
        self.extern_libraries.add(extern.library)

        # Map Dash types to C types
        type_map = {
            "Int": "LONG",
            "String": "STRPTR",
            "Bool": "BOOL",
            "Pointer": "APTR",
            "Void": "void",
        }

        # Generate return type
        ret_type = type_map.get(extern.return_type, "LONG") if extern.return_type else "void"

        # Generate parameter list
        params = []
        for p in extern.params:
            if isinstance(p, FunctionParam):
                p_type = type_map.get(p.param_type, "LONG") if p.param_type else "LONG"
                params.append(f"{p_type} {p.name}")
            else:
                params.append(f"LONG {p}")
        params_str = ", ".join(params) if params else "void"

        proto = f"extern {ret_type} {extern.name}({params_str});"
        self.extern_protos.append(proto)

    def _generate_native_func(self, native: NativeFunc):
        """Generate native function with inline C code."""
        # Map Dash types to C types
        type_map = {
            "Int": "LONG",
            "String": "STRPTR",
            "Bool": "BOOL",
            "Pointer": "APTR",
            "Void": "void",
        }

        # Generate return type
        ret_type = type_map.get(native.return_type, "LONG") if native.return_type else "void"

        # Generate parameter list
        params = []
        for p in native.params:
            if isinstance(p, FunctionParam):
                p_type = type_map.get(p.param_type, "LONG") if p.param_type else "LONG"
                params.append(f"{p_type} {p.name}")
            else:
                params.append(f"LONG {p}")
        params_str = ", ".join(params) if params else "void"

        # Generate function prototype
        proto = f"{ret_type} {native.name}({params_str});\n"
        self.prototypes.append(proto)

        # Generate function with inline C body
        c_code = native.c_code.strip()
        func_code = f"""
{ret_type} {native.name}({params_str}) {{
{c_code}
}}
"""
        self.native_funcs.append(func_code)

    def _generate_struct_typedef(self, struct: StructDef):
        """Generate C typedef, constructor, destructor and methods for a struct."""
        # Mark that we're using reference counting
        self.uses_refcount = True

        type_map = {
            "Int": "LONG",
            "String": "STRPTR",
            "Bool": "BOOL",
            "Pointer": "APTR",
            "Void": "void"
        }

        # Use _Dash_ prefix to avoid collision with Amiga SDK types
        c_name = f"_Dash_{struct.name}"

        # Generate typedef
        fields = []
        for field in struct.fields:
            c_type = type_map.get(field.field_type, "LONG")
            fields.append(f"    {c_type} {field.name};")

        typedef = f"typedef struct {{\n"
        typedef += "\n".join(fields)
        typedef += f"\n}} {c_name};\n"

        # Generate constructor function using refcount allocation
        params = []
        assigns = []
        for field in struct.fields:
            c_type = type_map.get(field.field_type, "LONG")
            params.append(f"{c_type} {field.name}")
            assigns.append(f"    s->{field.name} = {field.name};")

        params_str = ", ".join(params)
        assigns_str = "\n".join(assigns)

        constructor = f"""
static {c_name}* _dash_struct_create_{struct.name}({params_str}) {{
    {c_name} *s = ({c_name} *)_dash_rc_alloc(sizeof({c_name}));
    if (!s) return NULL;
{assigns_str}
    return s;
}}
"""

        # Generate retain helper for this struct type
        retain_func = f"""
static void {struct.name}_retain({c_name} *obj) {{
    _dash_retain(obj);
}}
"""

        # Generate release helper for this struct type
        release_func = f"""
static void {struct.name}_release({c_name} *obj) {{
    _dash_release(obj);
}}
"""

        # Generate methods as separate functions
        methods_code = ""
        for method in struct.methods:
            methods_code += self._generate_struct_method(struct, method, type_map, c_name)

        self.struct_typedefs.append(typedef + constructor + retain_func + release_func + methods_code)

    def _generate_struct_method(self, struct: StructDef, method: StructMethod,
                                type_map: dict, c_name: str) -> str:
        """Generate a struct method as a standalone function."""
        # Determine return type
        if method.return_type:
            ret_type = type_map.get(method.return_type, "LONG")
        else:
            # Infer from return statements
            ret_type = "void"
            for stmt in method.body:
                if isinstance(stmt, ReturnStmt) and stmt.value:
                    ret_type = "LONG"  # Default inference
                    break

        # Build parameter list with self as first param
        params = [f"{c_name} *self"]
        for p in method.params:
            if isinstance(p, FunctionParam):
                p_type = type_map.get(p.param_type, "LONG") if p.param_type else "LONG"
                params.append(f"{p_type} {p.name}")
            else:
                params.append(f"LONG {p}")
        params_str = ", ".join(params)

        # Generate method body
        # Save current state
        old_struct_context = getattr(self, '_current_struct', None)
        self._current_struct = struct.name

        self.context.enter_scope()
        # Declare parameters in scope
        self.context.declare_var("self")
        for p in method.params:
            if isinstance(p, FunctionParam):
                self.context.declare_var(p.name)
            else:
                self.context.declare_var(p)

        body_code = ""
        for stmt in method.body:
            body_code += self._generate_statement(stmt)

        self.context.exit_scope()

        # Restore state
        self._current_struct = old_struct_context

        # Method name: StructName_methodname
        method_name = f"{struct.name}_{method.name}"

        return f"""
static {ret_type} {method_name}({params_str}) {{
{body_code}}}
"""

    def visit_WindowDef_global(self, node: WindowDef):
        """Generate global declaration for a window."""
        code = CTemplates.WINDOW_GLOBAL.substitute(name=node.name)
        self.emit_global(code)

    def visit_MainBlock(self, node: MainBlock):
        """Generate code for the main block."""
        for stmt in node.body:
            self.visit(stmt)

    def visit_WindowOpen(self, node: WindowOpen):
        """Generate code to open a window."""
        self.uses_windows = True  # Mark that we're using windows

        window_def = self.analysis.program.get_window(node.window_name)
        if not window_def:
            self.emit(f"    // Error: Window '{node.window_name}' not found\n")
            return

        # Collect IDCMP flags based on event handlers
        self.context.idcmp_flags.add("IDCMP_CLOSEWINDOW")
        # Add GadTools IDCMP flags if using gadgets
        if self.needs_gadtools:
            self.context.idcmp_flags.add("IDCMP_GADGETUP")
            self.context.idcmp_flags.add("IDCMP_GADGETDOWN")
            self.context.idcmp_flags.add("IDCMP_MOUSEMOVE")
            self.context.idcmp_flags.add("IDCMP_MENUPICK")
            self.context.idcmp_flags.add("IDCMP_REFRESHWINDOW")
        idcmp_str = " | ".join(sorted(self.context.idcmp_flags))

        # Get window properties with defaults
        code = CTemplates.WINDOW_OPEN.substitute(
            name=node.window_name,
            left=self._get_window_prop(window_def, "left", 50),
            top=self._get_window_prop(window_def, "top", 50),
            width=window_def.width,
            height=window_def.height,
            title=window_def.title,
            closegadget=self._get_window_prop(window_def, "closegadget", "TRUE"),
            dragbar=self._get_window_prop(window_def, "dragbar", "TRUE"),
            depthgadget=self._get_window_prop(window_def, "depthgadget", "TRUE"),
            idcmp_flags=idcmp_str
        )
        self.emit(code)

        # Initialize Area fill support if needed
        if self.needs_area:
            self.emit(f"    _dash_init_area(_dash_rp, {window_def.width}, {window_def.height});\n")

        self.context.opened_windows.append(node.window_name)
        self.context.current_window = node.window_name

    def visit_EventLoop(self, node: EventLoop):
        """Generate code for an event loop."""
        if not self.context.opened_windows:
            self.emit("    // Error: No open window for EventLoop\n")
            return

        window = self.context.current_window or self.context.opened_windows[-1]
        self.context.in_event_loop = True

        # Start event loop
        self.emit(CTemplates.EVENT_LOOP_START.substitute(window=window))

        # Generate handlers
        for handler in node.handlers:
            self.visit(handler)

        # End event loop - generate manually to insert Area cleanup
        self.emit("            }\n")  # Close switch
        self.emit("        }\n")      # Close inner while
        self.emit("    }\n")          # Close outer while

        # Cleanup Area fill resources if used
        if self.needs_area:
            self.emit("    _dash_cleanup_area();\n")

        # Close window safely and nullify _dash_win if it points to same window
        self.emit(f"    if (win_{window}) {{\n")
        self.emit(f"        if (_dash_win == win_{window}) _dash_win = NULL;\n")
        self.emit(f"        CloseWindow(win_{window});\n")
        self.emit(f"        win_{window} = NULL;\n")
        self.emit(f"    }}\n")

        self.context.in_event_loop = False

    def visit_OnCloseHandler(self, node: OnCloseHandler):
        """Generate code for On Close handler."""
        handler_code = []
        for stmt in node.body:
            handler_code.append(self._generate_statement(stmt))

        code = CTemplates.ON_CLOSE_HANDLER.substitute(
            handler_code="".join(handler_code)
        )
        self.emit(code)

    def visit_OnKeyHandler(self, node: OnKeyHandler):
        """Generate code for On Key handler."""
        self.context.idcmp_flags.add("IDCMP_RAWKEY")

        handler_code = []
        for stmt in node.body:
            handler_code.append(self._generate_statement(stmt))

        # Map key to Amiga raw key code (simplified)
        keycode = self._key_to_rawkey(node.key)

        code = CTemplates.ON_KEY_HANDLER.substitute(
            keycode=keycode,
            handler_code="".join(handler_code)
        )
        self.emit(code)

    def visit_PrintStmt(self, node: PrintStmt):
        """Generate code for Print statement."""
        indent = self.context.indent
        if isinstance(node.message, StringLiteral):
            # String literal - print directly
            self.emit(f'{indent}Printf({node.message.value});\n{indent}Printf("\\n");\n')
        elif self._is_string_constant(node.message):
            # String constant (macro) - print directly, macro expands to string literal
            value = self._generate_expression(node.message)
            self.emit(f'{indent}Printf({value});\n{indent}Printf("\\n");\n')
        elif self._is_string_expr(node.message):
            # String expression - use %s format
            value = self._generate_expression(node.message)
            self.emit(f'{indent}Printf("%s", {value});\n{indent}Printf("\\n");\n')
        elif self._is_char_expr(node.message):
            # Character expression - use %c format
            value = self._generate_expression(node.message)
            self.emit(f'{indent}Printf("%c", {value});\n{indent}Printf("\\n");\n')
        else:
            # Expression - print as number
            value = self._generate_expression(node.message)
            self.emit(f'{indent}Printf("%ld", {value});\n{indent}Printf("\\n");\n')

    def visit_WaitStmt(self, node: WaitStmt):
        """Generate code for Wait statement."""
        seconds = node.seconds.value if isinstance(node.seconds, NumberLiteral) else str(node.seconds)
        code = CTemplates.WAIT_STMT.substitute(seconds=seconds)
        self.emit(code)

    def visit_StopStmt(self, node: StopStmt):
        """Generate code for Stop statement."""
        self.emit(CTemplates.STOP_STMT)

    def visit_Comment(self, node: Comment):
        """Skip comments in code generation."""
        pass

    def visit_VarDecl(self, node: VarDecl):
        """Generate code for variable declaration."""
        value = self._generate_expression(node.initial_value) if node.initial_value else "0"
        indent = self.context.indent

        if isinstance(node.initial_value, StringLiteral):
            var_type = "STRPTR"
        elif isinstance(node.initial_value, BoolLiteral):
            var_type = "BOOL"
        else:
            var_type = "LONG"

        self.context.declare_var(node.name)
        self.emit(f"{indent}{var_type} {node.name} = {value};\n")

    def visit_VarAssign(self, node: VarAssign):
        """Generate code for variable assignment (first use declares)."""
        indent = self.context.indent

        # Special handling for array literals
        if isinstance(node.value, ArrayLiteral):
            self.uses_arrays = True
            self.var_types[node.name] = "array"
            self._generate_array_init(node.name, node.value, indent)
            return

        value = self._generate_expression(node.value)
        var_type = self._infer_c_type(node.value)

        # Track variable type for string/struct detection
        if var_type == "STRPTR" or self._is_string_expr(node.value):
            self.var_types[node.name] = "string"
        elif var_type == "char":
            self.var_types[node.name] = "char"
        elif var_type.startswith("_Dash_") and var_type.endswith(" *"):
            # Struct pointer - track as struct:StructName
            struct_name = var_type[6:-2]  # Remove "_Dash_" prefix and " *" suffix
            self.var_types[node.name] = f"struct:{struct_name}"
        else:
            self.var_types[node.name] = "number"

        if not self.context.is_declared(node.name):
            # First assignment - declare the variable
            self.context.declare_var(node.name)
            self.emit(f"{indent}{var_type} {node.name} = {value};\n")
        else:
            # Reassignment to existing variable
            self.emit(f"{indent}{node.name} = {value};\n")

    def _generate_array_init(self, name: str, array: ArrayLiteral, indent: str):
        """Generate array initialization code."""
        size = len(array.elements)

        if not self.context.is_declared(name):
            self.context.declare_var(name)
            self.emit(f"{indent}DashArrayLong *{name} = DashArray_create({size});\n")
        else:
            self.emit(f"{indent}{name} = DashArray_create({size});\n")

        # Set each element
        for i, elem in enumerate(array.elements):
            value = self._generate_expression(elem)
            self.emit(f"{indent}DashArray_set({name}, {i}, {value});\n")

    def _infer_c_type(self, node) -> str:
        """Infer C type from an AST node."""
        if isinstance(node, StringLiteral):
            return "STRPTR"
        elif isinstance(node, BoolLiteral):
            return "BOOL"
        elif isinstance(node, ArrayLiteral):
            return "DashArrayLong *"
        elif isinstance(node, ArrayAccess):
            # Check if accessing a string or array
            if self._is_string_expr(node.array):
                return "char"  # Character access
            return "LONG"  # Array elements are LONG
        elif isinstance(node, PropertyAccess):
            if node.property == "length":
                return "LONG"
            return "LONG"
        elif isinstance(node, BinaryOp):
            # String concatenation returns string
            if node.operator == "+" and self._is_string_expr(node.left):
                return "STRPTR"
            return "LONG"
        elif isinstance(node, VarRef):
            # Check tracked variable types
            var_type = self.var_types.get(node.name)
            if var_type == "string":
                return "STRPTR"
            elif var_type and var_type.startswith("struct:"):
                struct_name = var_type[7:]  # Remove "struct:" prefix
                return f"_Dash_{struct_name} *"
            return "LONG"
        elif isinstance(node, FunctionCall):
            # Check if it's a struct constructor
            struct = self.analysis.program.get_struct(node.name)
            if struct:
                return f"_Dash_{struct.name} *"
            return "LONG"
        else:
            return "LONG"

    def visit_IfStmt(self, node: IfStmt):
        """Generate code for if statement."""
        condition = self._generate_expression(node.condition)
        indent_base = self.context.indent

        # Generate then body with new scope
        self.context.indent_level += 1
        self.context.enter_scope()
        then_body = ""
        for s in node.then_body:
            then_body += self._generate_statement(s)
        self.context.exit_scope()
        self.context.indent_level -= 1

        if node.else_body:
            # Generate else body with new scope
            self.context.indent_level += 1
            self.context.enter_scope()
            else_body = ""
            for s in node.else_body:
                else_body += self._generate_statement(s)
            self.context.exit_scope()
            self.context.indent_level -= 1

            code = CTemplates.IF_ELSE_STMT.substitute(
                indent_base=indent_base,
                condition=condition,
                then_body=then_body,
                else_body=else_body
            )
        else:
            code = CTemplates.IF_STMT.substitute(
                indent_base=indent_base,
                condition=condition,
                then_body=then_body
            )
        self.emit(code)

    def visit_WhileStmt(self, node: WhileStmt):
        """Generate code for while statement."""
        condition = self._generate_expression(node.condition)
        indent_base = self.context.indent

        # Generate body with new scope
        self.context.indent_level += 1
        self.context.enter_scope()
        body = ""
        for s in node.body:
            body += self._generate_statement(s)
        self.context.exit_scope()
        self.context.indent_level -= 1

        code = CTemplates.WHILE_STMT.substitute(
            indent_base=indent_base,
            condition=condition,
            body=body
        )
        self.emit(code)

    def visit_ForRangeStmt(self, node: ForRangeStmt):
        """Generate code for for-range statement."""
        start = self._generate_expression(node.start)
        end = self._generate_expression(node.end)
        indent = self.context.indent

        # Generate body with new scope
        self.context.indent_level += 1
        self.context.enter_scope()
        self.context.declare_var(node.variable)  # Loop var is declared by for
        body = ""
        for s in node.body:
            body += self._generate_statement(s)
        self.context.exit_scope()
        self.context.indent_level -= 1

        code = CTemplates.FOR_RANGE_STMT.substitute(
            indent=indent,
            var=node.variable,
            start=start,
            end=end,
            body=body
        )
        self.emit(code)

    def visit_ForEachStmt(self, node: ForEachStmt):
        """Generate code for for-each statement."""
        self.uses_arrays = True
        array = self._generate_expression(node.iterable)
        indent = self.context.indent

        # Generate body with new scope
        self.context.indent_level += 1
        self.context.enter_scope()
        self.context.declare_var(node.variable)  # Loop var is declared by for
        body = ""
        for s in node.body:
            body += self._generate_statement(s)
        self.context.exit_scope()
        self.context.indent_level -= 1

        code = CTemplates.FOR_EACH_STMT.substitute(
            indent=indent,
            var=node.variable,
            array=array,
            body=body
        )
        self.emit(code)

    def visit_FunctionDef(self, node: FunctionDef):
        """Generate code for function definition."""
        self.current_function = node

        # Type map for annotations
        type_map = {
            "Int": "LONG",
            "String": "STRPTR",
            "Bool": "BOOL",
            "Pointer": "APTR",
            "Void": "void",
        }

        # Determine return type - use explicit if provided, otherwise infer
        if node.return_type:
            return_type = type_map.get(node.return_type, "LONG")
        else:
            return_type = self._infer_function_return_type(node)

        # Generate parameter list with optional types
        param_strs = []
        param_names = []
        for p in node.params:
            if isinstance(p, FunctionParam):
                p_type = type_map.get(p.param_type, "LONG") if p.param_type else "LONG"
                param_strs.append(f"{p_type} {p.name}")
                param_names.append(p.name)
            else:
                param_strs.append(f"LONG {p}")
                param_names.append(p)
        params = ", ".join(param_strs) if param_strs else "void"

        # Generate prototype
        proto = CTemplates.FUNCTION_PROTO.substitute(
            return_type=return_type,
            name=node.name,
            params=params
        )
        self.prototypes.append(proto)

        # Generate function body with new scope
        self.context.enter_scope()
        # Declare parameters in scope
        for p_name in param_names:
            self.context.declare_var(p_name)

        body = ""
        for s in node.body:
            body += self._generate_statement(s)

        self.context.exit_scope()

        # Generate function definition
        code = CTemplates.FUNCTION_DEF.substitute(
            return_type=return_type,
            name=node.name,
            params=params,
            body=body
        )
        self.functions.append(code)
        self.current_function = None

    def _infer_function_return_type(self, node: FunctionDef) -> str:
        """Infer function return type by looking at return statements."""
        for stmt in node.body:
            if isinstance(stmt, ReturnStmt) and stmt.value is not None:
                return self._infer_c_type(stmt.value)
            # TODO: Also check nested blocks for return statements
        return "void"

    def visit_ReturnStmt(self, node: ReturnStmt):
        """Generate code for return statement."""
        indent = self.context.indent
        if node.value:
            value = self._generate_expression(node.value)
            self.emit(f"{indent}return {value};\n")
        else:
            self.emit(f"{indent}return;\n")

    def visit_ArrayAssign(self, node: ArrayAssign):
        """Generate code for array element assignment."""
        self.uses_arrays = True
        indent = self.context.indent
        array_name = self._generate_expression(node.array)
        index = self._generate_expression(node.index)
        value = self._generate_expression(node.value)
        self.emit(f"{indent}DashArray_set({array_name}, {index}, {value});\n")

    def visit_FunctionCall(self, node: FunctionCall):
        """Generate code for function call (as statement)."""
        # Check if it's a builtin function
        builtin = get_builtin(node.name)
        if builtin:
            c_code = self._generate_builtin_call(builtin, node.arguments)
            indent = self.context.indent
            self.emit(f"{indent}{c_code};\n")
            return

        args = ", ".join(self._generate_expression(a) for a in node.arguments)
        code = CTemplates.FUNCTION_CALL.substitute(name=node.name, args=args)
        self.emit(code)

    def generic_visit(self, node: ASTNode):
        """Handle unknown nodes."""
        self.emit(f"    // TODO: {node.__class__.__name__}\n")

    # === Helper Methods ===

    def _generate_statement(self, node: ASTNode) -> str:
        """Generate code for a statement and return it as a string."""
        old_code = self.main_code
        self.main_code = []
        self.visit(node)
        result = "".join(self.main_code)
        self.main_code = old_code
        return result

    def _generate_expression(self, node: ASTNode) -> str:
        """Generate code for an expression."""
        if isinstance(node, StringLiteral):
            return node.value
        elif isinstance(node, NumberLiteral):
            return str(node.value)
        elif isinstance(node, BoolLiteral):
            return "TRUE" if node.value else "FALSE"
        elif isinstance(node, VarRef):
            return node.name
        elif isinstance(node, BinaryOp):
            # Check if this is string concatenation
            if node.operator == "+" and self._is_string_expr(node.left):
                self.uses_strings = True
                left = self._generate_expression(node.left)
                right = self._generate_expression(node.right)
                return f"DashString_concat({left}, {right})"
            left = self._generate_expression(node.left)
            right = self._generate_expression(node.right)
            op = self._translate_operator(node.operator)
            return f"({left} {op} {right})"
        elif isinstance(node, UnaryOp):
            operand = self._generate_expression(node.operand)
            if node.operator == "not":
                return f"(!{operand})"
            return f"({node.operator}{operand})"
        elif isinstance(node, FunctionCall):
            # Check if it's a builtin function
            builtin = get_builtin(node.name)
            if builtin:
                return self._generate_builtin_call(builtin, node.arguments)
            # Check if it's a struct constructor
            struct = self.analysis.program.get_struct(node.name)
            if struct:
                return self._generate_struct_instantiation(struct, node.arguments)
            args = ", ".join(self._generate_expression(a) for a in node.arguments)
            return f"{node.name}({args})"
        elif isinstance(node, ArrayLiteral):
            self.uses_arrays = True
            return self._generate_array_literal(node)
        elif isinstance(node, ArrayAccess):
            # Check if accessing a string or array
            if self._is_string_expr(node.array):
                self.uses_strings = True
                str_name = self._generate_expression(node.array)
                index = self._generate_expression(node.index)
                return f"DashString_char_at({str_name}, {index})"
            self.uses_arrays = True
            array_name = self._generate_expression(node.array)
            index = self._generate_expression(node.index)
            return f"DashArray_get({array_name}, {index})"
        elif isinstance(node, ArraySlice):
            # Array slicing - generates a new array
            self.uses_arrays = True
            array_name = self._generate_expression(node.array)
            start = self._generate_expression(node.start) if node.start else "0"
            if node.end:
                end = self._generate_expression(node.end)
            else:
                end = f"DashArray_length({array_name})"
            return f"DashArray_slice({array_name}, {start}, {end})"
        elif isinstance(node, PropertyAccess):
            obj = self._generate_expression(node.object)
            if node.property == "length":
                # Check if it's a string or array
                if self._is_string_expr(node.object):
                    self.uses_strings = True
                    return f"DashString_length({obj})"
                self.uses_arrays = True
                return f"DashArray_length({obj})"
            # Check if object is self (struct member access)
            if isinstance(node.object, SelfRef):
                return f"self->{node.property}"
            # Check if object is a struct variable
            if isinstance(node.object, VarRef):
                var_type = self.var_types.get(node.object.name)
                if var_type and var_type.startswith("struct:"):
                    return f"{obj}->{node.property}"
            # Generic property access as pointer dereference
            return f"{obj}->{node.property}"
        elif isinstance(node, MemberAccess):
            # Check if it's an enum value access (Direction.UP)
            enum = self.analysis.program.get_enum(node.object_name)
            if enum:
                # Return the enum value name: EnumName_ValueName
                return f"{node.object_name}_{node.member}"

            # Handle member access like player.x or arr.length
            if node.member == "length":
                # Could be array or string length
                var_type = self.var_types.get(node.object_name)
                if var_type == "string":
                    self.uses_strings = True
                    return f"DashString_length({node.object_name})"
                self.uses_arrays = True
                return f"DashArray_length({node.object_name})"
            # Struct member access - use pointer notation
            return f"{node.object_name}->{node.member}"
        elif isinstance(node, LambdaFunc):
            # Generate an anonymous function and return its pointer
            return self._generate_lambda(node)
        elif isinstance(node, FunctionRef):
            # Return pointer to named function
            return f"(APTR){node.name}"
        elif isinstance(node, SelfRef):
            # Reference to self in struct method
            return "self"
        elif isinstance(node, MethodCall):
            # Method call on struct instance: player.move(5, 0) -> Player_move(player, 5, 0)
            return self._generate_method_call(node)
        else:
            return f"/* unknown expr: {type(node).__name__} */"

    def _generate_builtin_call(self, builtin: BuiltinFunc, arguments: List[ASTNode]) -> str:
        """Generate C code for a builtin function call."""
        # Track library requirements
        if builtin.needs_gfx:
            self.needs_gfx = True
        if builtin.needs_dos:
            pass  # DOS is always included
        if builtin.needs_asl:
            self.needs_asl = True
        if builtin.needs_fileio:
            self.needs_fileio = True
        if builtin.needs_gadtools:
            self.needs_gadtools = True
        if builtin.needs_audio:
            self.needs_audio = True
        if builtin.needs_area:
            self.needs_area = True
        if builtin.needs_reaction:
            self.needs_reaction = True
        if builtin.needs_rtg:
            self.needs_rtg = True
            self.needs_gfx = True  # RTG also requires graphics.library

        # Track arena usage
        if builtin.name.startswith("arena_"):
            self.uses_arenas = True

        # Track refcount usage for manual retain/release
        if builtin.name in ("refcount", "retain", "release"):
            self.uses_refcount = True

        # Build parameter mapping
        param_values = {}
        for i, param_name in enumerate(builtin.params):
            if i < len(arguments):
                param_values[param_name] = self._generate_expression(arguments[i])
            else:
                param_values[param_name] = "0"  # Default for missing params

        # Substitute into template
        template = Template(builtin.c_template)
        return template.substitute(param_values)

    def _generate_struct_instantiation(self, struct: StructDef, arguments: List[ASTNode]) -> str:
        """Generate C code for struct instantiation."""
        # Generate inline struct allocation and initialization
        # This creates a compound literal that allocates on the stack
        type_map = {"Int": "LONG", "String": "STRPTR", "Bool": "BOOL"}

        parts = []
        for i, field in enumerate(struct.fields):
            if i < len(arguments):
                val = self._generate_expression(arguments[i])
            elif field.default_value:
                val = self._generate_expression(field.default_value)
            else:
                c_type = type_map.get(field.field_type, "LONG")
                val = "0" if c_type != "STRPTR" else "NULL"
            parts.append(val)

        # Use heap allocation with AllocMem for Amiga
        init_values = ", ".join(parts)
        return f"_dash_struct_create_{struct.name}({init_values})"

    def visit_MemberAssign(self, node: MemberAssign):
        """Generate code for member assignment (player.x = 10)."""
        indent = self.context.indent
        value = self._generate_expression(node.value)
        self.emit(f"{indent}{node.object_name}->{node.member} = {value};\n")

    def visit_MethodCall(self, node: MethodCall):
        """Generate code for method call as statement."""
        indent = self.context.indent
        call_code = self._generate_method_call(node)
        self.emit(f"{indent}{call_code};\n")

    def _generate_method_call(self, node: MethodCall) -> str:
        """Generate C code for a method call on a struct instance."""
        # Get the object and determine its type
        obj_expr = self._generate_expression(node.object)

        # Try to determine struct type from the object
        struct_name = None
        if isinstance(node.object, VarRef):
            var_type = self.var_types.get(node.object.name)
            if var_type and var_type.startswith("struct:"):
                struct_name = var_type[7:]  # Remove "struct:" prefix

        if struct_name:
            # Known struct type - generate StructName_methodname(obj, args...)
            args = [obj_expr]
            args.extend(self._generate_expression(a) for a in node.arguments)
            args_str = ", ".join(args)
            return f"{struct_name}_{node.method}({args_str})"
        else:
            # Unknown type - try generic approach or fallback
            # This could happen with self.method() calls inside struct methods
            if isinstance(node.object, SelfRef):
                # Inside a struct method, use current struct context
                current_struct = getattr(self, '_current_struct', None)
                if current_struct:
                    args = ["self"]
                    args.extend(self._generate_expression(a) for a in node.arguments)
                    args_str = ", ".join(args)
                    return f"{current_struct}_{node.method}({args_str})"

            # Fallback - emit as function pointer call
            args = [obj_expr]
            args.extend(self._generate_expression(a) for a in node.arguments)
            args_str = ", ".join(args)
            return f"/* method call */ {node.method}({args_str})"

    def _is_string_constant(self, node: ASTNode) -> bool:
        """Check if an expression is a string constant (macro)."""
        if isinstance(node, VarRef):
            const = self.analysis.program.get_constant(node.name)
            if const and isinstance(const.value, StringLiteral):
                return True
        return False

    def _is_string_expr(self, node: ASTNode) -> bool:
        """Check if an expression evaluates to a string."""
        if isinstance(node, StringLiteral):
            return True
        elif isinstance(node, VarRef):
            # Check if it's a string variable
            if self.var_types.get(node.name) == "string":
                return True
            # Check if it's a string constant (already handled by _is_string_constant)
            const = self.analysis.program.get_constant(node.name)
            if const and isinstance(const.value, StringLiteral):
                return True
            return False
        elif isinstance(node, BinaryOp) and node.operator == "+":
            return self._is_string_expr(node.left)
        elif isinstance(node, FunctionCall):
            # Could check function return types, for now assume not string
            return False
        elif isinstance(node, MemberAccess):
            # Check if the struct field is a string type
            var_type = self.var_types.get(node.object_name)
            if var_type and var_type.startswith("struct:"):
                struct_name = var_type[7:]  # Remove "struct:" prefix
                struct = self.analysis.program.get_struct(struct_name)
                if struct:
                    field = struct.get_field(node.member)
                    if field and field.field_type == "String":
                        return True
            return False
        return False

    def _is_char_expr(self, node: ASTNode) -> bool:
        """Check if an expression evaluates to a character."""
        if isinstance(node, VarRef):
            return self.var_types.get(node.name) == "char"
        elif isinstance(node, ArrayAccess):
            return self._is_string_expr(node.array)
        return False

    def _generate_array_literal(self, node: ArrayLiteral) -> str:
        """Generate code for array literal - returns a temp variable name."""
        # This is tricky - we need to generate initialization code
        # For now, we'll generate inline and hope the context handles it
        # Better approach would be to generate a helper call
        size = len(node.elements)
        if size == 0:
            return "DashArray_create(0)"

        # Generate inline initialization
        # This works because C allows compound literals
        elements_str = ", ".join(self._generate_expression(e) for e in node.elements)
        # We'll return a call that creates and initializes the array
        # This requires storing in a temp, which is handled by assignment
        return f"DashArray_create({size}) /* init: {elements_str} */"

    def _generate_lambda(self, node: LambdaFunc) -> str:
        """Generate code for lambda/anonymous function - returns function pointer."""
        # Generate unique name for this lambda
        self.lambda_counter += 1
        lambda_name = f"_dash_lambda_{self.lambda_counter}"

        # Build parameter list
        params = []
        for p in node.params:
            if isinstance(p, FunctionParam):
                c_type = self._dash_type_to_c(p.param_type) if p.param_type else "LONG"
                params.append(f"{c_type} {p.name}")
            else:
                params.append(f"LONG {p}")

        param_str = ", ".join(params) if params else "void"

        # Generate function body
        if node.is_expression:
            # Single expression body (arrow function)
            expr = self._generate_expression(node.body)
            body = f"    return {expr};"
            return_type = "LONG"  # Assume LONG for now
        else:
            # Block body
            body_lines = []
            for stmt in node.body:
                stmt_code = self._generate_statement(stmt, "    ")
                if stmt_code:
                    body_lines.append(stmt_code)
            body = "\n".join(body_lines)
            return_type = "void"  # Assume void for block body without explicit return

        # Generate the lambda function
        func_code = f"""
static {return_type} {lambda_name}({param_str}) {{
{body}
}}
"""
        self.lambda_funcs.append(func_code)

        # Return function pointer
        return f"(APTR){lambda_name}"

    def _translate_operator(self, op: str) -> str:
        """Translate Dash operators to C operators."""
        op_map = {
            "and": "&&",
            "or": "||",
            "not": "!",
            "==": "==",
            "!=": "!=",
            "<": "<",
            ">": ">",
            "<=": "<=",
            ">=": ">=",
        }
        return op_map.get(op, op)

    def _dash_type_to_c(self, dash_type: str) -> str:
        """Convert Dash type annotation to C type."""
        type_map = {
            "Int": "LONG",
            "String": "STRPTR",
            "Bool": "BOOL",
            "Pointer": "APTR",
            "Void": "void",
            "Func": "APTR",
        }
        return type_map.get(dash_type, "LONG")

    def _get_window_prop(self, window: WindowDef, name: str, default) -> str:
        """Get a window property value or default."""
        prop = window.get_property(name)
        if prop is None:
            return str(default)
        if isinstance(prop, NumberLiteral):
            return str(prop.value)
        if isinstance(prop, BoolLiteral):
            return "TRUE" if prop.value else "FALSE"
        if isinstance(prop, StringLiteral):
            return prop.value
        return str(default)

    def _key_to_rawkey(self, key: str) -> str:
        """Convert a key character to Amiga raw key code."""
        # Simplified mapping - would need full table for production
        key_map = {
            "ESC": "0x45",
            "SPACE": "0x40",
            "RETURN": "0x44",
            "q": "0x10", "Q": "0x10",
            "w": "0x11", "W": "0x11",
            "e": "0x12", "E": "0x12",
        }
        return key_map.get(key.strip('"'), "0x00")



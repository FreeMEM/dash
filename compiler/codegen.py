class MultiFileGenerator:
    """
    Generates multiple C files from Dash modules.

    For each module, generates:
    - module.h: Header with prototypes, types, constants
    - module.c: Implementation with functions

    For the main module, generates:
    - main.c: Entry point with runtime initialization
    """

    def __init__(self, analysis: AnalysisResult, modules: Dict, main_path: str):
        """
        Initialize the multi-file generator.

        Args:
            analysis: Semantic analysis result (for the merged AST)
            modules: Dict of path -> ModuleInfo from DashCompiler
            main_path: Path to the main .dash file
        """
        self.analysis = analysis
        self.modules = modules
        self.main_path = main_path
        # Use the single-file generator for individual module code generation
        self.base_generator = CodeGenerator(analysis)

    def generate(self) -> Dict[str, str]:
        """
        Generate all C files.

        Returns:
            Dict mapping filename to content.
            E.g., {"main.c": "...", "audio.h": "...", etc.}

        Phase 1 approach:
        - main.c contains ALL code (functions from all modules)
        - module.h files are generated for future separate compilation
        - No separate module.c files yet (to avoid duplication)
        """
        import os
        output_files: Dict[str, str] = {}

        # Determine which module is the main module
        main_module = self.modules.get(self.main_path)
        if not main_module:
            # Fallback: use single-file generation
            return {"main.c": self.base_generator.generate()}

        main_name = os.path.splitext(os.path.basename(self.main_path))[0]

        # Generate the main file with all code (existing behavior)
        # This includes all functions from all modules (merged AST)
        main_c = self.base_generator.generate()
        output_files[f"{main_name}.c"] = main_c

        # Generate header files for imported modules (for future use)
        # These headers are informational - the actual code is in main.c
        for path, module in self.modules.items():
            if path == self.main_path:
                continue  # Skip main module

            module_name = module.name

            # Generate header for this module (prototypes, types, constants)
            header_content = self._generate_module_header(module)
            output_files[f"{module_name}.h"] = header_content

            # NOTE: We don't generate separate .c files for modules yet
            # because all code is already in main.c (merged AST approach)
            # This will change in Phase 2 when we implement true module separation

        return output_files

    def _generate_module_header(self, module) -> str:
        """Generate a .h header file for a module."""
        lines = []
        guard_name = f"_FLOW_{module.name.upper()}_H_"

        lines.append(f"/* Generated by Dash Language Compiler */")
        lines.append(f"#ifndef {guard_name}")
        lines.append(f"#define {guard_name}")
        lines.append("")
        lines.append("#include <exec/types.h>")
        lines.append("")

        ast = module.ast

        # Export constants
        if ast.constants:
            lines.append("/* Constants */")
            for const in ast.constants:
                if not const.name.startswith("_"):
                    value = self._const_value_to_c(const.value)
                    lines.append(f"#define {const.name} {value}")
            lines.append("")

        # Export enums
        if ast.enums:
            lines.append("/* Enums */")
            for enum in ast.enums:
                if not enum.name.startswith("_"):
                    lines.extend(self._generate_enum_header(enum))
            lines.append("")

        # Export structs (forward declarations and typedefs)
        if ast.structs:
            lines.append("/* Structs */")
            for struct in ast.structs:
                if not struct.name.startswith("_"):
                    lines.append(f"typedef struct _Dash_{struct.name} _Dash_{struct.name};")
            lines.append("")

        # Export global variable declarations (extern)
        if ast.global_vars:
            lines.append("/* Global Variables */")
            for gvar in ast.global_vars:
                if not gvar.name.startswith("_"):
                    c_type = self._dash_type_to_c(gvar.var_type)
                    lines.append(f"extern {c_type} {gvar.name};")
            lines.append("")

        # Export function prototypes
        if ast.functions:
            lines.append("/* Function Prototypes */")
            for func in ast.functions:
                if not func.name.startswith("_"):
                    proto = self._generate_func_prototype(func)
                    lines.append(proto)
            lines.append("")

        # Export native function prototypes
        if ast.native_funcs:
            lines.append("/* Native Function Prototypes */")
            for native in ast.native_funcs:
                if not native.name.startswith("_"):
                    proto = self._generate_native_prototype(native)
                    lines.append(proto)
            lines.append("")

        lines.append(f"#endif /* {guard_name} */")
        lines.append("")

        return "\n".join(lines)

    def _generate_module_source(self, module) -> str:
        """Generate a .c source file for a module."""
        lines = []
        lines.append(f"/* Generated by Dash Language Compiler */")
        lines.append(f"/* Module: {module.name} */")
        lines.append("")
        lines.append(f'#include "{module.name}.h"')
        lines.append("#include <proto/exec.h>")
        lines.append("#include <proto/dos.h>")
        lines.append("")

        ast = module.ast

        # Include dependency headers
        for dep_path in module.dependencies:
            dep_name = os.path.splitext(os.path.basename(dep_path))[0]
            lines.append(f'#include "{dep_name}.h"')
        if module.dependencies:
            lines.append("")

        # Global variable definitions
        if ast.global_vars:
            lines.append("/* Global Variables */")
            for gvar in ast.global_vars:
                c_type = self._dash_type_to_c(gvar.var_type)
                init_val = self._get_default_init(c_type)
                lines.append(f"{c_type} {gvar.name} = {init_val};")
            lines.append("")

        # For now, we'll use the base generator for function bodies
        # This is a simplified version - full implementation would
        # generate each function individually

        # Native function implementations
        if ast.native_funcs:
            lines.append("/* Native Functions */")
            for native in ast.native_funcs:
                lines.extend(self._generate_native_impl(native))
            lines.append("")

        # TODO: Generate function implementations
        # For now, functions are still in the main file
        lines.append("/* Function implementations are in main.c for now */")
        lines.append("")

        return "\n".join(lines)

    def _generate_main_file(self, main_module, headers: List[str]) -> str:
        """Generate the main.c file with all includes."""
        # For now, use the base generator but add include statements
        # at the top for all module headers

        # Get the full generated code
        full_code = self.base_generator.generate()

        # Find the position after the initial includes
        header_section = []
        header_section.append("/* Module Headers */")
        for h in headers:
            header_section.append(f'#include "{h}"')
        header_section.append("")

        # Insert after the first block of #includes
        lines = full_code.split("\n")
        insert_pos = 0
        in_includes = False
        for i, line in enumerate(lines):
            if line.startswith("#include"):
                in_includes = True
            elif in_includes and not line.startswith("#include") and line.strip():
                insert_pos = i
                break

        if insert_pos > 0:
            lines = lines[:insert_pos] + header_section + lines[insert_pos:]

        return "\n".join(lines)

    def _const_value_to_c(self, value) -> str:
        """Convert a constant value to C representation."""
        if isinstance(value, NumberLiteral):
            return str(value.value)
        elif isinstance(value, StringLiteral):
            return value.value
        elif isinstance(value, BoolLiteral):
            return "TRUE" if value.value else "FALSE"
        return "0"

    def _dash_type_to_c(self, dash_type: str) -> str:
        """Convert Dash type to C type."""
        type_map = {
            "Int": "LONG",
            "String": "STRPTR",
            "Bool": "BOOL",
            "Pointer": "APTR",
            "Void": "void",
        }
        return type_map.get(dash_type, "LONG")

    def _get_default_init(self, c_type: str) -> str:
        """Get default initialization for a C type."""
        if c_type in ("STRPTR", "APTR"):
            return "NULL"
        elif c_type == "BOOL":
            return "FALSE"
        return "0"

    def _generate_enum_header(self, enum) -> List[str]:
        """Generate enum typedef for header."""
        lines = []
        lines.append(f"typedef enum {{")
        current_value = 0
        for i, val in enumerate(enum.values):
            if val.value is not None:
                current_value = val.value
            comma = "," if i < len(enum.values) - 1 else ""
            lines.append(f"    {enum.name}_{val.name} = {current_value}{comma}")
            current_value += 1
        lines.append(f"}} {enum.name};")
        return lines

    def _generate_func_prototype(self, func) -> str:
        """Generate function prototype."""
        ret_type = self._dash_type_to_c(func.return_type) if func.return_type else "void"
        params = []
        for p in func.params:
            p_type = self._dash_type_to_c(p.param_type) if p.param_type else "LONG"
            params.append(f"{p_type} {p.name}")
        params_str = ", ".join(params) if params else "void"
        return f"{ret_type} {func.name}({params_str});"

    def _generate_native_prototype(self, native) -> str:
        """Generate native function prototype."""
        ret_type = self._dash_type_to_c(native.return_type) if native.return_type else "void"
        params = []
        for p in native.params:
            if hasattr(p, 'param_type'):
                p_type = self._dash_type_to_c(p.param_type) if p.param_type else "LONG"
                params.append(f"{p_type} {p.name}")
            else:
                params.append(f"LONG {p}")
        params_str = ", ".join(params) if params else "void"
        return f"{ret_type} {native.name}({params_str});"

    def _generate_native_impl(self, native) -> List[str]:
        """Generate native function implementation."""
        lines = []
        ret_type = self._dash_type_to_c(native.return_type) if native.return_type else "void"
        params = []
        for p in native.params:
            if hasattr(p, 'param_type'):
                p_type = self._dash_type_to_c(p.param_type) if p.param_type else "LONG"
                params.append(f"{p_type} {p.name}")
            else:
                params.append(f"LONG {p}")
        params_str = ", ".join(params) if params else "void"

        lines.append(f"{ret_type} {native.name}({params_str}) {{")
        lines.append(native.c_code.strip())
        lines.append("}")
        lines.append("")
        return lines
